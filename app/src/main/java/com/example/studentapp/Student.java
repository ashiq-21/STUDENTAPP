package com.example.studentapp;
public class Student {
    String key = "";
    String name = "";
    String place = "";
    String datetime = "";
    String description = "";
    String image1 = "";
    public Student(String key, String name, String place, String datetime,String description, String image1){
        this.key = key;
        this.name = name;
        this.place = place;
        this.datetime = datetime;
        this.description = description;
        this.image1 = image1;
    }
}